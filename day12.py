def check_region(matrix, type: str, perimeter: list, visited: list, i, j):

    # Check if already visited
    if (i,j) in visited:
        return
    
    visited.append((i, j))

    next_to_visit = []
    # It start in 4, and reducing by one with each neightbour
    actual_perimeter = 4

    # Check right
    if j < len(matrix[0])-1 and matrix[i][j+1] == type:
        if (i, j+1) not in visited:
            next_to_visit.append((i, j+1))
        actual_perimeter -= 1

    # Check left
    if j > 0 and matrix[i][j-1] == type:
        if (i, j-1) not in visited:
            next_to_visit.append((i, j-1))
        actual_perimeter -= 1

    # Check up
    if i > 0 and matrix[i-1][j] == type:
        if (i-1, j) not in visited:
            next_to_visit.append((i-1, j))
        actual_perimeter -= 1

    # Check down
    if i < len(matrix)-1 and matrix[i+1][j] == type:
        if (i+1, j) not in visited:
            next_to_visit.append((i+1, j))
        actual_perimeter -= 1

